N = int(input())

# DP 테이블 생성
dp = [[0] * 2 for _ in range(N + 1)]

# 초기값 설정
dp[1][1] = 1  # 1로 끝나는 1자리 이친수
dp[1][0] = 0  # 0으로 끝나는 1자리 이친수는 없음

# 점화식에 따른 DP 계산
for i in range(2, N + 1):
    dp[i][0] = dp[i-1][0] + dp[i-1][1]  # 0으로 끝나는 경우
    dp[i][1] = dp[i-1][0]               # 1로 끝나는 경우

# N자리 이친수의 개수
print(dp[N][0] + dp[N][1])
